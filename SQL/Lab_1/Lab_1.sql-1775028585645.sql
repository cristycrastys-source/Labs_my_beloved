-- Задание 1.1. Базовый поиск (Salespeople)
-- ============================================================================
-- 1. Вывести username первых 10 нанятых женщин-продавцов (gender='Female').
select username
from salespeople
where gender = 'Female'
limit 10;


-- 2. Сортировка по hire_date.
select username
from salespeople
where gender = 'Female'
order by hire_date
limit 10;

-- 3. Повторить для мужчин.
select username
from salespeople
where gender = 'Male'
order by hire_date
limit 10;

-- ============================================================================
-- Задание 1.2. Работа с клиентами (Customers)
-- ============================================================================
-- 1. Все email клиентов из 'FL', сортировка по алфавиту.
select email
from customers
where state = 'FL'
order by email;

-- 2. Имена и email клиентов из 'New York City', 'NY'.
select 
first_name, 
last_name, 
email
from customers
where city = 'New York City' and state = 'NY';

-- 3. Сортировка: Фамилия, затем Имя.
select first_name, last_name, email
from customers
where city = 'New York City' and state = 'NY'
order by 
last_name, 
first_name;

-- 4. Все клиенты с телефоном, сортировка по дате регистрации.
select *
from products
limit 10;

select *
from customers
where phone is not NULL and phone != ''
order by date_added;

