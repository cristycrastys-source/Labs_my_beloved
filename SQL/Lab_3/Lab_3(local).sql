-- Найти среднеквадратичное отклонение продаж.
select round(stddev(sales_amount)::numeric, 2) as sales_stddev,
round(avg(sales_amount)::numeric, 2) as avg_sales,
min(sales_amount) as min_sales,
max(sales_amount) as max_sales,
count(*) as total_sales_countalter 
from sales;

-- ============================================================================
-- Задание 2.2. (Группировка GROUP BY)
-- ============================================================================
-- Сгруппировать продукты по первой букве типа, найти среднюю цену.
select left(product_type, 1) as first_letter,
round(avg(base_msrp)::numeric, 2) as avg_price,
count(*) as product_count,
min(base_msrp) as min_price,
max(base_msrp) as max_price
from products
where product_type is not null
and product_type != ''
group by left(product_type, 1)
order by first_letter;

-- ============================================================================
-- Задание 2.3. (Фильтрация HAVING и Аналитика)
-- ============================================================================
-- Вывести штаты, где разница между max и min продажей > 1000.
select c.state,
count(s.customer_id) as sales_count,
round(min(s.sales_amount)::numeric, 2) as min_sale,
round(max(s.sales_amount)::numeric, 2) as max_sale,
round((max(s.sales_amount) - min(s.sales_amount))::numeric, 2) as sale_range
from sales s
inner join customers c on s.customer_id = c.customer_id
where c.state is not null
and c.state != ''
group by c.state
having (max(s.sales_amount) - min(s.sales_amount)) > 1000
order by sale_range desc;