-- Найти типы продуктов (product_type), средняя базовая цена (base_msrp) которых превышает 500 у.е., и отсортировать их по убыванию средней цены.
select product_type,
round(avg(base_msrp), 2) as avg_price
from products p group by p.product_type 
having avg(base_msrp) > 500
order by avg_price desc;

